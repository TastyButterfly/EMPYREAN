                    <br><h3>Gameplay</h3>
                    As with Crytek's previous game Far Cry, Crysis is a first-person shooter game with many ways to meet objectives. 
                    The player controls a special forces soldier codenamed Nomad. The player's weapons can be customized without pausing the 
                    flow of time, for example changing firing modes, changing scopes or adding sound suppressors.<br>
                    <br><h3>Weapons</h3>
                    The game features assault rifles, sub-machine guns, pistols, missile launchers, shotguns, miniguns, sniper rifles, 
                    gauss rifles (coilguns), the MOAC (a machine gun style alien weapon which fires high-velocity ice shards), and the TAC 
                    gun (a hand-held nuclear grenade launcher). Most weapons can be modified with attachments; these attachments may be given 
                    to the player by default, acquired from picked-up weapons, or purchased in multiplayer.
